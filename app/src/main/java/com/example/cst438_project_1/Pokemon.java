package com.example.cst438_project_1;

import java.util.List;

public class Pokemon {
    private String name;

    private int id;

    private List<String> types;

    private int height;

    private int weight;

    public String getName() {
        return name;
    }

    public int getId() {
        return id;
    }

    public List<String> getTypes() {
        return types;
    }

    public int getHeight() {
        return height;
    }

    public int getWeight() {
        return weight;
    }
}
