package com.example.cst438_project_1;

public class Pokemon {
    private String name;
    private String height;
    private String weight;
    private String id;

    public Pokemon() {
    }

    public String getName() {
        return name;
    }

    public String getHeight() {
        return height;
    }

    public String getWeight() {
        return weight;
    }

    public String getId() {
        return id;
    }
}


